// [OUT]: Admin panel resources, custom dashboard, settings pages, and contact topbar copy / 后台资源、自定义仪表板、设置页面与顶部栏联系信息
// [POS]: Backend Filament admin panel provider / 后端 Filament 后台面板提供者
